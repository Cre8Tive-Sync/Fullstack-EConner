export function getCategoryForPlace(categoryId) {
  return CATEGORIES.find((c) => c.id === categoryId) || CATEGORIES[0]
}

const DAY_NAMES = ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat']

export function formatOperatingHours(hours) {
  if (!hours || hours.length === 0) return ''
  // Check if all days have null times (24/7 or unspecified)
  const allNull = hours.every((d) => d.open_time == null && d.close_time == null)
  if (allNull) {
    const allOpen = hours.every((d) => !d.is_closed)
    return allOpen ? 'Open 24 hours' : 'Hours vary'
  }
  // Group by open/close time
  const groups = {}
  for (const d of hours) {
    if (d.is_closed) continue
    const key = `${d.open_time}-${d.close_time}`
    if (!groups[key]) groups[key] = []
    groups[key].push(DAY_NAMES[d.day_of_week])
  }
  const parts = Object.entries(groups).map(([key, days]) => {
    const [open, close] = key.split('-')
    const dayStr = days.length === 7 ? 'Daily' : days.join(', ')
    return `${dayStr} ${open}–${close}`
  })
  return parts.join('; ')
}

